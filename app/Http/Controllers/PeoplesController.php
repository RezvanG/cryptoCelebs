    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'username' => ['required', 'string', 'max:255', 'unique:people'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:people']
        ]);

        // Sign up a new person
        $people = new People;
        $people->username = $request->input('username');
        $people->email = $request->input('email');
        $people->save();
        return response()->json(['success'=>'Thank you for signing up!']);

       // return redirect('/posts')->with('success', 'Thank you for signing up!');
    }

